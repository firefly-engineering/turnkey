func TestWriteTOML_MissingHash(t *testing.T) {
	deps := []Dependency{
		{ImportPath: "github.com/foo/bar", Version: "v1.0.0", GoSumHash: "h1:somehash="},
	}

	var buf bytes.Buffer
	opts := OutputOptions{
		IncludeHeader:         false,
		IncludeHashWarning:    true,
		IncludeRegenerateHint: false,
	}

	err := WriteTOML(&buf, deps, opts)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	output := buf.String()
	// Should have empty hash with comment
	if !strings.Contains(output, `hash = ""`) {
		t.Error("expected empty hash")
	}
	if !strings.Contains(output, "go.sum h1:somehash=") {
		t.Error("expected go.sum hash in comment")
	}
	// Should have warning about missing hashes
	if !strings.Contains(output, "IMPORTANT") {
		t.Error("expected hash warning")
	}
}

func TestWriteTOML_WithHeader(t *testing.T) {
	deps := []Dependency{}

	var buf bytes.Buffer
	opts := OutputOptions{
		IncludeHeader:         true,
		IncludeHashWarning:    false,
		IncludeRegenerateHint: true,
	}

	err := WriteTOML(&buf, deps, opts)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	output := buf.String()
	if !strings.Contains(output, "Auto-generated by godeps-gen") {
		t.Error("missing header")
	}
	if !strings.Contains(output, "To regenerate:") {
		t.Error("missing regenerate hint")
	}
	if !strings.Contains(output, "schema_version = 2") {
		t.Error("missing or incorrect schema version")
	}
	if !strings.Contains(output, "Key format: deps.") {
		t.Error("missing key format comment")
	}
}

