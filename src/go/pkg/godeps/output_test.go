func TestWriteTOML_MultipleDependencies(t *testing.T) {
	deps := []Dependency{
		{ImportPath: "github.com/aaa/first", Version: "v1.0.0", NixHash: "sha256-a="},
		{ImportPath: "github.com/bbb/second", Version: "v1.1.0", NixHash: "sha256-b="},
		{ImportPath: "github.com/ccc/third", Version: "v1.2.0", NixHash: "sha256-c=", Indirect: true},
	}

	var buf bytes.Buffer
	opts := OutputOptions{}

	err := WriteTOML(&buf, deps, opts)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	output := buf.String()
	// Check all deps are present
	if !strings.Contains(output, "github.com/aaa/first") {
		t.Error("missing first dep")
	}
	if !strings.Contains(output, "github.com/bbb/second") {
		t.Error("missing second dep")
	}
	if !strings.Contains(output, "github.com/ccc/third") {
		t.Error("missing third dep")
	}
}

